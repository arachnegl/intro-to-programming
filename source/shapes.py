import turtle

def square():
    turtle.forward(50)
    turtle.left(90)
    turtle.forward(50)
    turtle.left(90)
    turtle.forward(50)
    turtle.left(90)
    turtle.forward(50)
    turtle.left(90)

def rectangle():
    turtle.forward(100)
    turtle.left(90)
    turtle.forward(50)
    turtle.left(90)
    turtle.forward(100)
    turtle.left(90)
    turtle.forward(50)
    turtle.left(90)

def equilateral_triangle():
    turtle.forward(100)
    turtle.left(180-60)
    turtle.forward(100)
    turtle.left(180-60)
    turtle.forward(100)
    turtle.left(180-60)
